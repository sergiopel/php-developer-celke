var valorUm = 8;
var valorDois = 9;

if (valorUm == valorDois){
    document.write(valorUm + " é igual a " + valorDois + "<br>");
}

if (valorUm != valorDois){
    document.write(valorUm + " é diferente de " + valorDois + "<br>");
}

if (valorUm < valorDois){
    document.write(valorUm + " é menor que " + valorDois + "<br>");
}

if (valorUm > valorDois){
    document.write(valorUm + " é maior que " + valorDois + "<br>");
}

if (valorUm <= valorDois){
    document.write(valorUm + " é menor ou igual que " + valorDois + "<br>");
}

if (valorUm >= valorDois){
    document.write(valorUm + " é maior ou igual que " + valorDois + "<br>");
}
